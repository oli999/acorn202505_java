package test.main;
/*
 *  5. 대입연산자 테스트
 *  
 *  =, +=, -=, *=, /=, %= 
 */
public class MainClass07 {
	public static void main(String[] args) {
		int num=10;
		//num = num+2; 아래는 이코드의 줄인 표현
		num += 2;	
		
		//num = num-3; 아래는 이코드의 줄인 표현
		num -= 3;	
		
		//num = num*4; 아래는 이코드의 줄인 표현
		num *= 4;
		
		//num = num/5; 아래는 이코드의 줄인 표현
		num /= 5;
		
		//num = num%6; 아래는 이코드의 줄인 표현
		num %= 6;		
	}
}
